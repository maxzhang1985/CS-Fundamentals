package HashTable;

import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.*;

class
ValidSudokuTest {
    ValidSudoku _vs = new ValidSudoku();

    @Test
    public void test1() {
        char[][] board = new char[][]{{'.','8','7','6','5','4','3','2','1'},
                                      {'2','.','.','.','.','.','.','.','.'},
                                      {'3','.','.','.','.','.','.','.','.'},
                                      {'4','.','.','.','.','.','.','.','.'},
                                      {'5','.','.','.','.','.','.','.','.'},
                                      {'6','.','.','.','.','.','.','.','.'},
                                      {'7','.','.','.','.','.','.','.','.'},
                                      {'8','.','.','.','.','.','.','.','.'},
                                      {'9','.','.','.','.','.','.','.','.'}};
        assertEquals(true, _vs.isValidSudoku(board));
    }

    @Test
    public void test2() {
        char[][] board = new char[][]{{'.','8','7','6','5','4','3','2','1'},
                                      {'2','8','.','.','.','.','.','.','.'},
                                      {'3','.','.','.','.','.','.','.','.'},
                                      {'4','.','.','.','.','.','.','.','.'},
                                      {'5','.','.','.','.','.','.','.','.'},
                                      {'6','.','.','.','.','.','.','.','.'},
                                      {'7','.','.','.','.','.','.','.','.'},
                                      {'8','.','.','.','.','.','.','.','.'},
                                      {'9','.','.','.','.','.','.','.','.'}};
        assertEquals(false, _vs.isValidSudoku(board));
    }
}